#include<stdio.h>

int main() {

int a = 8, b = 10;

printf("a = %d and b = %d \n\n",a,b);

printf("a i.e. 8, 8>>1 (right shift operation) = %d \n", a>>1);
printf("a i.e. 8, 8>>2 (right shift operation) = %d \n", a>>2);

printf("b i.e. 10, 10>>1 (right shift operation) = %d \n", b>>1);
printf("b i.e. 10, 10>>1 (right shift operation) = %d \n", b>>2);

return 0;
}