#include<stdio.h>

int main() {

int a = 4, b = 5;

printf("a = %d and b = %d \n\n",a,b);

printf("a i.e. 4, 4<<1 (left shift operation) = %d \n", a<<1);
printf("a i.e. 4, 4<<2 (left shift operation) = %d \n", a<<2);

printf("b i.e. 5, 5<<1 (left shift operation) = %d \n", b<<1);
printf("b i.e. 5, 5<<1 (left shift operation) = %d \n", b<<2);

return 0;
}